CREATE TABLE IF NOT EXISTS projects (
    id              TEXT PRIMARY KEY,
    internal_id     BIGINT GENERATED BY DEFAULT AS IDENTITY UNIQUE,
    name            TEXT NOT NULL,
    description     TEXT,
    status          TEXT NOT NULL DEFAULT 'active',
    settings        JSONB DEFAULT '{}',
    created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at      TIMESTAMPTZ NOT NULL DEFAULT NOW()
);
CREATE UNIQUE INDEX IF NOT EXISTS idx_projects_name ON projects(name);

CREATE TABLE IF NOT EXISTS api_keys (
    id          TEXT PRIMARY KEY,
    project_id  TEXT NOT NULL REFERENCES projects(id) ON DELETE CASCADE,
    name        TEXT NOT NULL,
    secret_hash TEXT NOT NULL,
    scopes      TEXT[] NOT NULL DEFAULT '{}',
    expire_at   TIMESTAMPTZ,
    enabled     BOOLEAN NOT NULL DEFAULT TRUE,
    created_at  TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at  TIMESTAMPTZ NOT NULL DEFAULT NOW()
);
CREATE INDEX IF NOT EXISTS idx_api_keys_project ON api_keys(project_id);

CREATE TABLE IF NOT EXISTS console_admins (
    id            TEXT PRIMARY KEY,
    email         TEXT NOT NULL UNIQUE,
    password_hash TEXT NOT NULL,
    role          TEXT NOT NULL DEFAULT 'owner',
    created_at    TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at    TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS document_databases (
    id          TEXT PRIMARY KEY,
    project_id  TEXT NOT NULL REFERENCES projects(id) ON DELETE CASCADE,
    name        TEXT NOT NULL,
    created_at  TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at  TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    UNIQUE(project_id, name)
);

CREATE TABLE IF NOT EXISTS document_collections (
    id                 TEXT PRIMARY KEY,
    database_id        TEXT NOT NULL REFERENCES document_databases(id) ON DELETE CASCADE,
    project_id         TEXT NOT NULL REFERENCES projects(id) ON DELETE CASCADE,
    name               TEXT NOT NULL,
    document_security  BOOLEAN NOT NULL DEFAULT TRUE,
    permissions        TEXT[] DEFAULT '{}',
    created_at         TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at         TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    UNIQUE(database_id, name)
);
CREATE INDEX IF NOT EXISTS idx_document_collections_project ON document_collections(project_id);

CREATE TABLE IF NOT EXISTS document_attributes (
    id            TEXT PRIMARY KEY,
    collection_id TEXT NOT NULL REFERENCES document_collections(id) ON DELETE CASCADE,
    key           TEXT NOT NULL,
    type          TEXT NOT NULL,
    size          INT,
    required      BOOLEAN NOT NULL DEFAULT FALSE,
    is_array      BOOLEAN NOT NULL DEFAULT FALSE,
    default_value TEXT,
    options       JSONB DEFAULT '{}',
    created_at    TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    UNIQUE(collection_id, key)
);

CREATE TABLE IF NOT EXISTS document_indexes (
    id            TEXT PRIMARY KEY,
    collection_id TEXT NOT NULL REFERENCES document_collections(id) ON DELETE CASCADE,
    type          TEXT NOT NULL,
    attributes    TEXT[] NOT NULL,
    orders        TEXT[] DEFAULT '{}',
    created_at    TIMESTAMPTZ NOT NULL DEFAULT NOW()
);
